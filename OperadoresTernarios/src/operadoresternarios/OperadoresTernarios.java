package operadoresternarios;

/**
 *
 * @author Tiago
 */
public class OperadoresTernarios {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
       int n1, n2, r;
       n1 = 8;
       n2 = 9;
       r = (n1>n2)?0:3;//operadores ternarios (maior ou menor) ? : ;
       System.out.println(r);
    }
    
}
